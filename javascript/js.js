// random color 
document.getElementById("random-color-btn").addEventListener("click", function () {
    function randomC() {
        let l = "0123456789ABCDEF0123456789ABCDEF";
        let c = "#";
        for (let i = 0; i < 6; i++) {
            c += l[Math.floor(Math.random() * 32)];
        }
        return c;
    }

    document.body.style.backgroundColor = randomC();
});

